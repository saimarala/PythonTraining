# Arthimatice operators
a = 10
b = 5
print(a + b)  # add 15
print(a - b)  # sub 5
print(a * b)  # multiply 50
print(a / b)  # 2.0
print(a // b)  # quotient 2
print(a % b)  # remainder 0
print(a ** b)  # exponent values 5**10=100000

# Relational operators/ comparison operators
# > < >= <= !=
# Relational operators always returns boolean value True/False
print("Relational operators")
a = 5
b = 10
print(a > b)  # False
print(a < b)  # True
print(a == b)  # False
print(a != b)  # True
print(a <= b)  # True
print(a >= b)  # False

# Logical operators
# Logical operators always work with boolean values
# Logical operators always returns boolean value True/False
# and or not
print("Logical operators")

a = True
b = False

print(a and b)  # False
print(a or b)  # True
print(not a)  # False
